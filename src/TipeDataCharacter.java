public class TipeDataCharacter {
    public static void main(String[] args) {
        char a = 'A';
        char r = 'R';
        char i = 'I';
        char f = 'F';

        System.out.println(a);  // A
        System.out.println(r);  // R
        System.out.println(i);  // I
        System.out.println(f);  // F

    }

}